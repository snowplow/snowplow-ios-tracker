import Foundation

/// A specification for the appearance and interaction of a window.
@objc(SPWindowStyle)
public enum WindowStyle: Int {
    /// Default window style.
    case automatic
    /// Hides both the windowâ€™s title and the backing of the titlebar area.
    case hiddenTitleBar
    /// Plain window style.
    case plain
    /// Displays the title bar section of the window.
    case titleBar
    /// Creates a 3D volumetric window.
    case volumetric
}

extension WindowStyle {
    var value: String {
        switch self {
        case .automatic:
            return "automatic"
        case .hiddenTitleBar:
            return "hiddenTitleBar"
        case .plain:
            return "plain"
        case .titleBar:
            return "titleBar"
        case .volumetric:
            return "volumetric"
        }
    }
}

/**
 Properties for the VisionOS window group entity.
 Entity schema: `iglu:com.apple.swiftui/window_group/jsonschema/1-0-0`
 */
@objc(SPWindowGroupEntity)
public class WindowGroupEntity: SelfDescribingJson {
    
    /// Randomly generated ID for the current window within the group. Generated by the tracker if not provided by the user.
    @objc
    public var id: UUID
    
    /// A localized string key to use for the window's title in system menus and in the window's title bar. Provide a title that describes the purpose of the window.
    @objc
    public var titleKey: String?
    
    /// A string that uniquely identifies the window group. Identifiers must be unique among the window groups in your app.
    @objc
    public var windowGroupId: String?
    
    /// A specification for the appearance and interaction of a window.
    public var windowStyle: WindowStyle?
    
    @objc
    override public var data: [String : Any] {
        get {
            var data: [String : Any] = [
                "id": id.uuidString
            ]
            if let titleKey = titleKey { data["title_key"] = titleKey }
            if let windowGroupId = windowGroupId { data["window_group_id"] = windowGroupId }
            if let windowStyle = windowStyle { data["window_style"] = windowStyle.value }
            return data
        }
        set {}
    }
    
    /// - Parameter id: Randomly generated ID for the current window within the group. Generated by the tracker if not provided by the user.
    /// - Parameter titleKey: A localized string key to use for the window's title in system menus and in the window's title bar.
    /// - Parameter windowGroupId: A string that uniquely identifies the window group.
    /// - Parameter windowStyle: A specification for the appearance and interaction of a window.
    public init(
        id: UUID = UUID(),
        titleKey: String? = nil,
        windowGroupId: String? = nil,
        windowStyle: WindowStyle? = nil
    ) {
        self.id = id
        self.titleKey = titleKey
        self.windowGroupId = windowGroupId
        self.windowStyle = windowStyle
        super.init(schema: swiftuiWindowGroup, andData: [:])
    }
    
    /// - Parameter id: Randomly generated ID for the current window within the group. Generated by the tracker if not provided by the user.
    /// - Parameter titleKey: A localized string key to use for the window's title in system menus and in the window's title bar.
    /// - Parameter windowGroupId: A string that uniquely identifies the window group.
    @objc
    public init(
        id: UUID = UUID(),
        titleKey: String? = nil,
        windowGroupId: String? = nil
    ) {
        self.id = id
        self.titleKey = titleKey
        self.windowGroupId = windowGroupId
        super.init(schema: swiftuiWindowGroup, andData: [:])
    }
    
    /// A specification for the appearance and interaction of a window.
    @objc
    public func windowStyle(_ windowStyle: WindowStyle) -> Self {
        self.windowStyle = windowStyle
        return self
    }
}
